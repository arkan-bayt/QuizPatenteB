// Helper: get questions by chapter IDs
export function getQuestionsByChapters(chapterIds: number[]): QuizQuestion[] {
  return ALL_QUESTIONS.filter((q) => chapterIds.includes(q.chapter));
}

// Helper: get unique topics
export function getUniqueTopics(): string[] {
  const topics = new Set(ALL_QUESTIONS.map((q) => q.topic));
  return Array.from(topics).sort();
}

// Helper: get chapters by topic
export function getChaptersByTopic(topic: string): Chapter[] {
  return CHAPTERS.filter((ch) => ch.topic === topic);
}
